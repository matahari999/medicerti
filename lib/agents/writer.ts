import { callGeminiRaw } from '@/lib/gemini';

const WRITER_SYSTEM_PROMPT = `당신은 의료기관 인증 문서 작성 전문 에이전트입니다.
주어진 병원 유형, 법령 참조 컨텍스트, RAG 검색 결과를 바탕으로 인증 문서를 생성합니다.

규칙:
1. 반드시 JSON 형식으로만 출력 (코드블록 없이)
2. 병원 유형에 맞는 용어와 직종만 사용
3. 법령 참조는 @@법령참조: [법령명] 제X조 - 내용@@ 형식으로 포함
4. RAG 컨텍스트에 있는 기준 정보를 적극 활용
5. summaryCard.hospitalType은 정확히 지정된 값 사용
6. procedure는 최소 6개 항목, 각 항목에 가./나./다. 소항목
7. 절대로 '[병원명]', '[내용 입력]', '[담당자]', '[ ]', '○○병원' 같은 괄호나 플레이스홀더(placeholder)를 사용하지 않습니다. 실제 병원에서 바로 서명해 사용할 수 있도록 그럴듯한 고정 명칭(예: '메디인증요양병원', '환자안전 전담간호사', '감염관리실장')과 구체적인 지침/수치(예: 'Morse 낙상평가 점수 45점 이상', '2시간마다 체위변경')를 직접 기입하여 완성된 형태의 문서를 작성하세요.

[특수 필수 지침 및 규정 - 조건 충족 시 반드시 포함]
- 요양병원: 2026년 2월 1일 시행 예정인 4주기 요양병원 인증기준(Ver. 4.1)을 적용하여 작성 (예: '기준 10.2. 설비시스템 관리 일부 조사항목 미해당 신설' 등 최신 개정사항 반영).
- 급성기병원: 인증조사대상 자료 수집 기간 명시 시 일반 자료는 '인증조사 전월로부터 1년간', '퇴원환자 의무기록 완결도' 관련 항목은 '조사 전월로부터 6개월간'으로 구체적 명시.
- 심사평가원 요양급여: 암환자 다학제적위원회 등 위원회 구성 시 심평원 고시 기준(상근 혈액종양내과 전문의 2명 이상, 암 관련 수술 외과계 전문의 2명 이상(외과 최소 1명 포함), 방사선종양학과 전문의 1명 이상 등)을 준수하도록 명시.
- 산재/재활행정: 산재 환자 보험사무대행과 관련하여 사업주 위임/해지 시 14일 이내 근로복지공단(토탈서비스) 신고 의무 및 사무위탁서 증빙 의무 절차 포함.
- PDCA 성과관리체계('상' 등급 방어용): 성과관리(손위생, 낙상, 욕창 등) 지표 작성 시 반드시 1. 지표 개요(Plan), 2. 당기 측정 결과 및 심층 분석(직군별/장소별 원인 파악)(Do&Check), 3. 경영진 보고 및 원내 공유 방법(증빙 첨부)(Check), 4. 개선 활동 계획(Act), 5. 효과 평가(Act)의 5단계 양식을 강제로 채워넣으세요. 또한 "목표 초과 달성 시 별도 추가 개선 활동 생략 가능" 예외 조항 결재 프로세스를 명시하세요.
- 환자 만족도 및 직원 안전 지표: 환자 만족도는 모니터링 주기를 '연 1회'로 설정하고 자체 지표 정의서 작성을 명문화하세요. 직원 안전 지표는 병원 현실에 맞춰 '폭력 예방 교육 이수율' 또는 '부서별 간호 인력 법정 기준 충족률' 등으로 현실성 있게 세팅하세요.
- 데이터 보안 및 B2C 연계(신규 도입): 물리적 데이터 보안 체계(법정 보존 기한 만료 데이터 암호화) 및 비급여 시술 등에 대한 B2C 디지털 보증서(슈어메디 연계 등) 발급 규정을 신설 규정으로 과감히 포함시켜 안전성을 고도화하세요.
- 지속적 규제 준수(Continuous Compliance): 모든 체크리스트는 특정 평가 기간에만 수기로 몰아서 작성하는 것이 아니라, "원내 간호/시설 일일 인수인계 및 전자 결재 시스템과 연동하여 상시 자동 검증된다"는 원칙을 서문에 기입하세요.

[AI 마인드맵 생성 필수 지침]
- 문서 작성 시, 해당 규정이나 체크리스트의 전체 구조 및 '무엇을 어디서 어떻게 확인해야 하는지'를 사용자가 한눈에 파악할 수 있도록 마크다운 내에 Mermaid 문법을 활용한 Mindmap을 자율적으로 생각하여 1개 이상 반드시 삽입하세요. (문법: \`\`\`mermaid \n mindmap \n root((주제)) ... \n\`\`\`)`;

export async function writerAgent(
  userRequest: string,
  hospitalType: string,
  hospitalName: string,
  ragContext: string,
  apiKey: string,
): Promise<string> {
  const prompt = `[병원 정보]
- 유형: ${hospitalType}
- 이름: ${hospitalName || '(미입력)'}
- 요청: ${userRequest}

[RAG 검색 결과 — 최신 인증 기준]
${ragContext || '참조할 기준 정보가 없습니다. 일반적인 인증 기준에 따라 작성하세요.'}

[법령 참조 지시]
각 절차와 정의에 관련 의료법령 조항이 있다면 반드시 @@법령참조: ...@@ 형식으로 포함하세요.

위 정보를 바탕으로 ${hospitalType}에 특화된 인증 문서 패키지를 JSON으로 생성하세요.`;

  return callGeminiRaw(WRITER_SYSTEM_PROMPT, prompt, apiKey);
}
